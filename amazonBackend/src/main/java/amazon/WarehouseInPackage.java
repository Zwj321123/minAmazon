package amazon;

public class WarehouseInPackage {
    private int id;
    private int x;
    private int y;

    public WarehouseInPackage(int id){
        this.id = id;
        this.x = id;
        this.y = id;
    }

    public int getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
